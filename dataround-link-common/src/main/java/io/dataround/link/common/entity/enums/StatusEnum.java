package io.dataround.link.common.entity.enums;

/**
 * common status enum
 * 
 * @author yuehan124@163.com
 * @date 2025-04-04
 */
public enum StatusEnum {

    OK("1", "Ok"), DISABLE("2", "Error");

    private final String code;
    private final String info;

    StatusEnum(String code, String info) {
        this.code = code;
        this.info = info;
    }

    public String getCode() {
        return code;
    }

    public String getInfo() {
        return info;
    }
}
